    protected void addAdditionalSaveData(CompoundTag compound) {

    }

    @Override
    protected double getDefaultGravity() {
        return 0.06;
    }

    @Override
    public void tick() {
        super.tick();
        Vec3 vec3 = this.getDeltaMovement();
        HitResult hitresult = ProjectileUtil.getHitResultOnMoveVector(this, this::canHitEntity);
        if (hitresult.getType() != HitResult.Type.MISS && !net.neoforged.neoforge.event.EventHooks.onProjectileImpact(this, hitresult))
            this.hitTargetOrDeflectSelf(hitresult);
        double d0 = this.getX() + vec3.x;
        double d1 = this.getY() + vec3.y;
        double d2 = this.getZ() + vec3.z;
        this.updateRotation();
        if (this.level().getBlockStates(this.getBoundingBox()).noneMatch(BlockBehaviour.BlockStateBase::isAir)) {
            this.discard();
        } else if (this.isInWaterOrBubble()) {
            this.discard();
        } else {
            this.setDeltaMovement(vec3.scale(0.99F));
            this.applyGravity();
            this.setPos(d0, d1, d2);
        }

        for (int i = 0; i < 10; i++) {
            this.level().addParticle(ParticleTypes.SNOWFLAKE,
                    d0 + RandomUtil.randomValueBetween(-3, 3),
                    d1 + RandomUtil.randomValueBetween(-3, 3),
                    d2 + RandomUtil.randomValueBetween(-3, 3), vec3.x, vec3.y, vec3.z);
        }
    }

    @Override
    protected void onHitBlock(BlockHitResult result) {
        super.onHitBlock(result);
        if (!this.level().isClientSide) {
            Vec3 vec3 = this.position();
            this.level().explode(this, Explosion.getDefaultDamageSource(this.level(), this), null, vec3.x(), vec3.y(), vec3.z(), 3.0F, false, Level.ExplosionInteraction.TNT);
            this.discard();
        }
    }

